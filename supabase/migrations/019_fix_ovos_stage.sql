-- Обновить название этапа во всех существующих проектах
UPDATE project_stages
SET name      = 'Разработка ОВОС',
    stage_key = 'ovos'
WHERE stage_key = 'obos'
   OR name = 'Разработка ОБОС';

-- Обновить пункт чеклиста: убрать "инвестиции", добавить ОВОС
UPDATE stage_checklist_items
SET label = 'Оценка влияния на окружающую среду'
WHERE label = 'Разработка обоснования инвестиций'
  AND stage_id IN (
    SELECT id FROM project_stages
    WHERE name = 'Разработка ОВОС'
  );
